function ZhuE_ETu(MapID,InstanceID,RoleID,Type) --根据玩家等级刷新通缉恶徒或者喽(Type:0-喽，1-恶徒)

	local RoleLevel = role.GetRoleLevel(MapID, InstanceID, RoleID)
	RoleLevel = RoleLevel - 25
	local ETuIndex = math.floor(RoleLevel/5) + 1
	if ETuIndex > 16 then
	    ETuIndex = 16
	end
	local r = math.random(3)
	local ID = 0

	if Type == 0 then  --通缉恶徒
		if r == 1 then
			ID = CreatureETu[ETuIndex].ID1
		elseif r == 2 then
			ID = CreatureETu[ETuIndex].ID2
		elseif r == 3 then
			ID = CreatureETu[ETuIndex].ID3
		end
 	elseif Type == 1 then  --恶徒喽
		ID = CreatureETu[ETuIndex].ID0
	end

	return ID
end





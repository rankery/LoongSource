	local num1 = role.GetRoleItemNum(RoleID, 2612680)
	local num2 = role.GetRoleItemNum(RoleID, 2612681)
	local num3 = role.GetRoleItemNum(RoleID, 2612682)
	local num4 = role.GetRoleItemNum(RoleID, 2612683)
	local num5 = role.GetRoleItemNum(RoleID, 2612684)
	--判断有任一玄晶
	if num1 < 1 and num2 < 1 and num3 < 1 and num4 < 1 and num5 < 1 then
		local MsgID = msg.BeginMsgEvent()
		msg.AddMsgEvent(MsgID, 26, 227)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	end

	--判断本日任务接取次数是否已满
	--得到当前任务总任务环数
	local LoopNum = role.GetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_Loop"])
	local TotalNum = role.GetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_Total"])
	local UpdateTime = role.GetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_Update"])


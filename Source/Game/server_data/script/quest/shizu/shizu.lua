		TotalNum = 1
		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_Total"], TotalNum)
	end

	--检测任务上次更新日期，若与当前日期不一致，则重置总环数及更新日期
	local a = os.time()
	local CurTime = tonumber(os.date("%j"))
	if CurTime~=UpdateTime then
		TotalNum = 1

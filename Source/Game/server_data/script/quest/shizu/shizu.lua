		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_Count"],0)

	end
	--若本日已接取30次，则无法再接取
	if TotalNum > 30 then
		local MsgID = msg.BeginMsgEvent()
		msg.AddMsgEvent(MsgID, 26, 229)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	end

	-- 除去玩家身上玄晶
	if num1 > 0 then
		role.RemoveFromRole(MapID, InstanceID, RoleID, 2612680, 1, 101)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_StoneID"], 2612680)
	elseif num2 > 0 then
		role.RemoveFromRole(MapID, InstanceID, RoleID, 2612681, 1, 101)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_StoneID"], 2612681)
	elseif num3 > 0 then
		role.RemoveFromRole(MapID, InstanceID, RoleID, 2612682, 1, 101)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_StoneID"], 2612682)
	elseif num4 > 0 then
		role.RemoveFromRole(MapID, InstanceID, RoleID, 2612683, 1, 101)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_StoneID"], 2612683)
	elseif num5 > 0 then
		role.RemoveFromRole(MapID, InstanceID, RoleID, 2612684, 1, 101)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["ShiZuRand_StoneID"], 2612684)
	end

	return 1
end





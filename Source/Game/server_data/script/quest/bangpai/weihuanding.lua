--放弃任务
function x30003_OnCancel(MapID, InstanceID, QuestID, OwnerID)
	local LoopNum = role.GetRoleScriptData(OwnerID, 1, RoleDataType["AnDing"])
	local TotalNum = role.GetRoleScriptData(OwnerID, 1, RoleDataType["AnDing_Total"])
	local k = LoopNum % 10
	if k == 0 then
		k=10
	end
	--总任务环数加一，当前环数置为1
	role.SetRoleScriptData(OwnerID, 1, RoleDataType["AnDing"], LoopNum-k+1)
	role.SetRoleScriptData(OwnerID, 1, RoleDataType["AnDing_Total"], TotalNum+1)
	--设置当前挖宝任务Index为0(GT_INVALID)
	role.SetRoleScriptData(OwnerID, 1, RoleDataType["AnDing_Index"], 0)
end

function x30003_OnCheckAccept(MapID, InstanceID, QuestID, RoleID, NPCID)
	--判断玩家是否已加入帮派
	local GuildID = guild.GetRoleGuildID(RoleID)
	if GuildID==4294967295 then
		local MsgID = msg.BeginMsgEvent()
		msg.AddMsgEvent(MsgID, 26, 143)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	end

	--[[判断玩家帮派职位
	local GuildPos=guild.GetRoleGuildPos(GuildID, RoleID)
	if (GuildPos~=2) and (GuildPos~=3) and (GuildPos~=12) and (GuildPos~=13) and (GuildPos~=14) then
		local MsgID = msg.BeginMsgEvent()
		msg.AddMsgEvent(MsgID, 26, 145)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	end]]

	--判断背包空闲空间是否足够
	local FreeSize = role.GetBagFreeSize(RoleID)
	if(FreeSize < 1) then
		local MsgID = msg.BeginMsgEvent()
		msg.AddMsgEvent(MsgID, 26, 141)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	end

	--判断本日任务接取次数是否已满
	--得到当前任务总任务环数
	local LoopNum = role.GetRoleScriptData(RoleID, 1, RoleDataType["AnDing"])
	local TotalNum = role.GetRoleScriptData(RoleID, 1, RoleDataType["AnDing_Total"])
	local UpdateTime = role.GetRoleScriptData(RoleID, 1, RoleDataType["AnDing_Update"])

	--总环数为0时，设置总环数为第一环
	if 0 == LoopNum then
		LoopNum = 1
		role.SetRoleScriptData(RoleID, 1, RoleDataType["AnDing"], LoopNum)
	end
	if 0 == TotalNum then
		TotalNum = 1
		role.SetRoleScriptData(RoleID, 1, RoleDataType["AnDing_Total"], TotalNum)
	end

	--检测任务上次更新日期，若与当前日期不一致，则重置总环数及更新日期
	local CurTime = tonumber(os.date("%j"))
	if CurTime~=UpdateTime then
		role.SetRoleScriptData(RoleID, 1, RoleDataType["AnDing"], 1)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["AnDing_Total"], 1)
		role.SetRoleScriptData(RoleID, 1, RoleDataType["AnDing_Update"], CurTime)
	end
	--若本日已接取100次，则无法再接取
	if TotalNum > 20 then
		local MsgID = msg.BeginMsgEvent()
		msg.AddMsgEvent(MsgID, 26, 147)
		msg.DispatchRoleMsgEvent(RoleID, MsgID)
		return 0
	end

	return 1
end

